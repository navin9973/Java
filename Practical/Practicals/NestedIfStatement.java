package Practicals;
public class NestedIfStatement 
{
	
	    public static void main(String[] args) 
	    {
	        int num = 10;

	        if (num > 5) 
	        {
	            if (num < 15) 
	            {
	                System.out.println("Number is between 5 and 15");
	            }
	        }
	    }
}
