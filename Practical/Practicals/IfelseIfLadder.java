package Practicals;
public class IfelseIfLadder
{
	    public static void main(String[] args) 
	    {
	        int num = 10;

	        if (num > 15) 
	        {
	            System.out.println("Number is greater than 15");
	        } else if (num > 10) 
	        {
	            System.out.println("Number is greater than 10 but less than or equal to 15");
	        } else if (num > 5) 
	        {
	            System.out.println("Number is greater than 5 but less than or equal to 10");
	        } else 
	        {
	            System.out.println("Number is 5 or less");
	        }
	    }
}
